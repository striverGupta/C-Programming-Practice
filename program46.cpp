//write a program to show use of bitwise operators:

#include <conio.h>
#include <stdio.h>
int main(){
	int a = 27,b= 39;
	
	printf("\n a&b =%d",a&b);
	printf("\n a |b=%d",a|b);
	printf("\n ~a =%d",~a);
	printf("\n ~b =%d",~b);
	printf("\n a^b =%d",a^b);
	printf("\n a<<b =%d",a<<1);
	printf("\n a>>b =%d",b>>1);
}
